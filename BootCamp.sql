 /****** Object:  SP Add Update Delete Customer ********/
 GO
CREATE PROCEDURE [dbo].[spAddUpdateDelete_Customer]
@CustomerID    INT,          
@EmailAddress  VARCHAR (255),
@FirstName     VARCHAR (60), 
@LastName      VARCHAR (60), 
@hardDelete	   BIT = 0         
AS
BEGIN
	BEGIN TRAN
		BEGIN TRY
			--==================================================================== DELETE
			IF(@hardDelete = 1) BEGIN	
				IF NOT EXISTS(SELECT NULL FROM tblCustomer) BEGIN
					SELECT [CustomerID] = @CustomerID, [ErroCode] = 0, [ErroMsg] = 'No Customer with specified customerID.'
					END ELSE 
					BEGIN 
						DELETE FROM tblCustomerInClass WHERE CustomerID = @CustomerID	
						DELETE FROM tblCustomerMetrics WHERE CustomerID = @CustomerID										
						DELETE FROM tblCustomer WHERE CustomerID = @CustomerID
						SELECT [CustomerID] = 0, [ErroCode] = 0, [ErroMsg] = 'Customer Deleted Sucessfully.'
					END
			--==================================================================== ADD
			END ELSE IF(@CustomerID = 0) BEGIN									
				IF NOT EXISTS(SELECT NULL FROM tblCustomer WHERE EmailAddress = @EmailAddress) BEGIN
					  insert into tblCustomer (EmailAddress, FirstName, LastName)
					   VALUES (@EmailAddress, @FirstName, @LastName)
					   SELECT [CustomerID] = @@IDENTITY, [ErroCode] = 0, [ErroMsg] = 'Customer Sucessfully Added.'
				END ELSE BEGIN
					SELECT [CustomerID] = 0, [ErroCode] = 1, [ErroMsg] = 'There is already a customer with the same email address.'
				END

			--==================================================================== UPDATE
			END ELSE IF(@CustomerID > 0) BEGIN
				IF		EXISTS(SELECT NULL FROM tblCustomer WHERE (EmailAddress = @EmailAddress ) AND (CustomerID = @CustomerID)) BEGIN

					UPDATE tblCustomer SET 
					EmailAddress = @EmailAddress, FirstName =@FirstName,
					LastName = @LastName
					WHERE CustomerID = @CustomerID
					SELECT [CustomerID] = @CustomerID, [ErroCode] = 0, [ErroMsg] = 'Customer Sucessfully updated.'

				END ELSE BEGIN
					SELECT [CustomerID] = @CustomerID, [ErroCode] = 1, [ErroMsg] = 'Update didn''t work. Possible issue with email address or customer id'
				END

			END ELSE BEGIN
				SELECT [CustomerID] = @CustomerID, [ErroCode] = 1, [ErroMsg] = 'Error... Contact Support.'
			END
		END TRY BEGIN CATCH
			IF(@@TRANCOUNT > 0) ROLLBACK TRAN;
		END CATCH
	IF(@@TRANCOUNT > 0) COMMIT TRAN;
END
GO


/****** Object:  SP Get Classes By Day ********/
CREATE PROCEDURE [dbo].[spGetClassByDay]
	@ClassDOW VARCHAR (60)
AS
	SELECT ClassDOW, ClassName, ClassTime, ClassStudio, l.LevelName AS [ClassLevel], c.InstructorName, ClassFocusName
	FROM vwClass As c
	JOIN tblLevel As l
	On c.LevelID = l.LevelID
	WHERE ClassDOW=@ClassDOW
	ORDER BY c.LevelID

GO

/****** Object:  SP Get Classes By Instructor ********/
CREATE PROCEDURE [dbo].[spGetClassByInstructor]
	@FirstName VARCHAR (60),
	@LastName VARCHAR (60)
AS
	SELECT i.InstructorID, i.FirstName +' '+ i.LastName AS [InstructorName], c.ClassName, c.ClassDOW, c.ClassTime, c.ClassStudio, l.LevelName AS [ClassLevel], ClassFocusName
	From vwClass c
	JOIN tblInstructor i ON
	c.InstructorID = i.InstructorID
	JOIN tblLevel As l
	On c.LevelID = l.LevelID
	WHERE i.FirstName = @FirstName AND i.LastName = @LastName
	ORDER BY c.ClassDOW
GO


/****** Object:  SP Get Classes By Level ********/
CREATE PROCEDURE [dbo].[spGetClassByLevel]
	@LevelID INT
AS
	SELECT ClassDOW, ClassName, ClassTime, ClassStudio, l.LevelName AS [ClassLevel], InstructorName, ClassFocusName
	FROM vwClass As c
	JOIN tblLevel As l
	On c.LevelID = l.LevelID
	WHERE c.LevelID = @LevelID
	ORDER BY c.ClassName

GO



/****** Object:  SP Get Classes By Focus ********/
CREATE PROCEDURE [dbo].[spGetClassByFocus]
	@ClassFocusID INT
AS
	SELECT  f.ClassFocusName, ClassDOW, ClassName, ClassTime, ClassStudio, l.LevelName AS [ClassLevel], InstructorName
	FROM vwClass As c
	JOIN tblLevel As l
	On c.LevelID = l.LevelID
	JOIN tblClassFocus f
	ON f.ClassFocusID = c.ClassFocusID
	WHERE c.ClassFocusID = @ClassFocusID
	ORDER BY c.ClassName, c.LevelID

GO

/****** Object:  SP Get Customers By Class ********/
CREATE PROCEDURE [dbo].[spGetCustomersByClass]
	@ClassName VARCHAR (60)
AS
	SELECT c.ClassName, c.ClassDOW, c.ClassTime, c.ClassStudio, cu.FirstName, cu.LastName 
	FROM tblCustomerInClass i
	JOIN tblCustomer cu ON
	i.CustomerID = cu.CustomerID
	JOIN tblClass c ON
	c.ClassID = i.ClassID
	WHERE c.ClassName = @ClassName
	ORDER BY c.ClassDOW, c.ClassTime, cu.LastName, cu.FirstName


GO

