/**********************OBJECT: Customer*********************/
CREATE TABLE tblCustomer (
  CustomerID		   INT            PRIMARY KEY   IDENTITY,
  EmailAddress         VARCHAR(255)   NOT NULL      UNIQUE,
  FirstName            VARCHAR(60)    NOT NULL,
  LastName             VARCHAR(60)    NOT NULL,
);

/**********************OBJECT: WeighIn*********************/
CREATE TABLE tblCustomerMetrics (
	CustomerID			 INT   REFERENCES tblCustomer (CustomerID),
	WeightInLbs		     FLOAT,
	HeightInInches		 FLOAT,
	WeighInDate          DATE	
);

/**********************OBJECT: Instructor*********************/
CREATE TABLE tblInstructor (
  InstructorID		   INT            PRIMARY KEY   IDENTITY,
  EmailAddress         VARCHAR(255)   NOT NULL      UNIQUE,
  FirstName            VARCHAR(60)    NOT NULL,
  LastName             VARCHAR(60)    NOT NULL
);

/**********************OBJECT: Level***********************/
CREATE TABLE tblLevel (
    LevelID   INT          PRIMARY KEY		IDENTITY,
    LevelName VARCHAR (50) NOT NULL
);

/**********************OBJECT: Focus************************/
CREATE TABLE tblClassFocus (
    ClassFocusID		 INT           PRIMARY KEY		IDENTITY,
    ClassFocusName	 VARCHAR (255) NOT NULL
);

/**********************OBJECT: Class*********************/
Create Table tblClass (
    ClassID		   INT            PRIMARY KEY		IDENTITY,
	ClassName      VARCHAR(60)    NOT NULL,
	ClassStudio    VARCHAR(60)    NOT NULL,
	InstructorID   INT            REFERENCES tblInstructor (InstructorID),
	ClassDOW       VARCHAR(60)    NOT NULL,
	ClassTime	   VARCHAR(60)	  NOT NULL,
	LevelID		   INT            REFERENCES tblLevel (LevelID),
	ClassFocusID   INT           REFERENCES tblClassFocus (ClassFocusID)
);

/**********************OBJECT: CustomerClasss*********************/
Create Table tblCustomerInClass (
    CustomerID	   INT   REFERENCES tblCustomer (CustomerID),
	ClassID		   INT	 REFERENCES tblClass (ClassID)
);





