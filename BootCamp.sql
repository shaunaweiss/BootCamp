/****** Object:  SP Get Latest Weigh In ********/
CREATE PROCEDURE [dbo].[spGetLatestWeighIn]
	@EmailAddress VARCHAR (255) 
AS
	SELECT TOP(1) c.FirstName, c.LastName, m.WeightInLbs, m.BMI, CONVERT(varchar(60), m.WeighInDate) AS WeighInDate
	FROM vwCustomerMetrics AS m
	JOIN tblCustomer AS c ON
	c.CustomerID = m.CustomerID
	WHERE c.EmailAddress = @EmailAddress
	ORDER BY m.WeighInDate DESC